private:
    std::vector<Sample> samples;

    static SPD make(double startNm, double stepNm, std::initializer_list<double> values) {
        std::vector<Sample> s;
        s.reserve(values.size());
        double lambda = startNm;
        for (double v : values) {
            s.push_back({lambda, v});
            lambda += stepNm;
        }
        return SPD(std::move(s));
    }
